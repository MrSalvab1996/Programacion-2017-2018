    public void operacion() {
        switch(calculo) {
            
            case 1: //Suma
                ans = num + Double.parseDouble(display.getText());
                display.setText(Double.toString(ans));
                break;
            case 2: //Resta
                ans = num - Double.parseDouble(display.getText());
                display.setText(Double.toString(ans));
                break;
            case 3: //Multi
                ans = num * Double.parseDouble(display.getText());
                display.setText(Double.toString(ans));
                break;
            case 4: //Div
                ans = num / Double.parseDouble(display.getText());
                display.setText(Double.toString(ans));
                break;
        
        }
    
    }
    
